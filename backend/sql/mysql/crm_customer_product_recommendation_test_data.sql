-- 按客户统计推荐数量
SELECT
  c.id AS customer_id,
  c.customer_name,
  COUNT(r.id) AS recommendation_count,
  SUM(CASE WHEN r.is_hot_sale = 1 THEN 1 ELSE 0 END) AS hot_sale_count,
  SUM(CASE WHEN r.is_match_customer = 1 THEN 1 ELSE 0 END) AS match_customer_count,
  SUM(CASE WHEN r.recommendation_status = 'purchased' THEN 1 ELSE 0 END) AS purchased_count
FROM crm_customer c
LEFT JOIN crm_customer_product_recommendation r ON c.id = r.customer_id
WHERE c.deleted = 0
GROUP BY c.id, c.customer_name
ORDER BY c.id;

-- 按推荐类型统计
SELECT
  recommendation_type,
  COUNT(*) AS count
FROM crm_customer_product_recommendation
GROUP BY recommendation_type
ORDER BY count DESC;

-- 按推荐状态统计
SELECT
  recommendation_status,
  COUNT(*) AS count
FROM crm_customer_product_recommendation
GROUP BY recommendation_status
ORDER BY count DESC;

-- 按产品类型统计
SELECT
  product_type,
  COUNT(*) AS count
FROM crm_customer_product_recommendation
GROUP BY product_type
ORDER BY count DESC;

-- 查看热销产品推荐
SELECT
  product_code,
  product_name,
  COUNT(*) AS recommendation_count,
  SUM(CASE WHEN recommendation_status = 'purchased' THEN 1 ELSE 0 END) AS purchase_count
FROM crm_customer_product_recommendation
WHERE is_hot_sale = 1
GROUP BY product_code, product_name
ORDER BY recommendation_count DESC
LIMIT 10;

-- 查看高匹配度推荐
SELECT
  recommendation_no,
  c.customer_name,
  r.product_name,
  r.match_degree,
  r.recommendation_score,
  r.recommendation_status
FROM crm_customer_product_recommendation r
JOIN crm_customer c ON r.customer_id = c.id
WHERE r.match_degree = 'high'
ORDER BY r.recommendation_score DESC
LIMIT 10;
