-- 对公客户项目信息表
-- 用于记录企业的项目台账,包括在建项目、投资项目、重大工程等,为客户营销提供参考

DROP TABLE IF EXISTS crm_company_project;
CREATE TABLE crm_company_project (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键ID',
    customer_id BIGINT NOT NULL COMMENT '客户ID（关联crm_customer表）',

    -- 项目基本信息
    project_code VARCHAR(50) COMMENT '项目编号',
    project_name VARCHAR(200) NOT NULL COMMENT '项目名称',
    project_type VARCHAR(50) NOT NULL COMMENT '项目类型（基建工程、房地产开发、技术改造、研发项目、并购重组、境外投资、产能扩张、环保治理等）',
    project_category VARCHAR(50) COMMENT '项目类别（自建、合作、收购、PPP等）',

    -- 项目规模
    total_investment DECIMAL(18,2) COMMENT '总投资额（元）',
    registered_capital DECIMAL(18,2) COMMENT '注册资本（元）',
    project_area DECIMAL(18,2) COMMENT '项目占地面积（平方米）',
    building_area DECIMAL(18,2) COMMENT '建筑面积（平方米）',

    -- 项目周期
    plan_start_date DATE COMMENT '计划开工日期',
    actual_start_date DATE COMMENT '实际开工日期',
    plan_complete_date DATE COMMENT '计划完工日期',
    actual_complete_date DATE COMMENT '实际完工日期',
    construction_period INT COMMENT '建设周期（月）',

    -- 项目状态
    project_status TINYINT DEFAULT 1 COMMENT '项目状态（1=筹备中 2=在建 3=已完工 4=运营中 5=暂停 6=终止）',
    progress_rate DECIMAL(5,2) COMMENT '项目进度（%）',

    -- 项目地点
    project_province VARCHAR(50) COMMENT '项目所在省份',
    project_city VARCHAR(50) COMMENT '项目所在城市',
    project_district VARCHAR(50) COMMENT '项目所在区县',
    project_address VARCHAR(500) COMMENT '项目详细地址',

    -- 资金来源与使用
    self_funding DECIMAL(18,2) COMMENT '自有资金（元）',
    bank_loan DECIMAL(18,2) COMMENT '银行贷款（元）',
    bond_financing DECIMAL(18,2) COMMENT '债券融资（元）',
    equity_financing DECIMAL(18,2) COMMENT '股权融资（元）',
    government_subsidy DECIMAL(18,2) COMMENT '政府补助（元）',
    other_funding DECIMAL(18,2) COMMENT '其他资金（元）',
    accumulated_investment DECIMAL(18,2) COMMENT '累计完成投资（元）',

    -- 银行融资需求
    financing_requirement DECIMAL(18,2) COMMENT '融资需求金额（元）',
    financing_purpose VARCHAR(500) COMMENT '融资用途',
    financing_status TINYINT COMMENT '融资状态（1=未申请 2=申请中 3=已批准 4=已放款 5=已拒绝）',
    our_bank_financing DECIMAL(18,2) COMMENT '我行融资金额（元）',
    other_bank_financing DECIMAL(18,2) COMMENT '他行融资金额（元）',

    -- 合作方信息
    partners VARCHAR(500) COMMENT '合作方（多个合作方用逗号分隔）',
    contractor VARCHAR(200) COMMENT '总承包商',
    designer VARCHAR(200) COMMENT '设计单位',
    supervisor VARCHAR(200) COMMENT '监理单位',

    -- 预期效益
    expected_revenue DECIMAL(18,2) COMMENT '预计年收入（元）',
    expected_profit DECIMAL(18,2) COMMENT '预计年利润（元）',
    payback_period DECIMAL(5,2) COMMENT '投资回收期（年）',
    irr_rate DECIMAL(8,4) COMMENT '内部收益率（%）',

    -- 风险评估
    risk_level VARCHAR(20) COMMENT '风险等级（低、中、高）',
    risk_factors VARCHAR(1000) COMMENT '主要风险因素',
    risk_mitigation VARCHAR(1000) COMMENT '风险缓释措施',

    -- 政府支持
    is_key_project TINYINT DEFAULT 0 COMMENT '是否重点项目（0=否 1=市级 2=省级 3=国家级）',
    government_approval VARCHAR(200) COMMENT '政府批文',
    policy_support VARCHAR(500) COMMENT '政策支持',

    -- 环保与合规
    environmental_approval TINYINT DEFAULT 0 COMMENT '环评批复（0=未申请 1=已批复 2=不需要）',
    land_approval TINYINT DEFAULT 0 COMMENT '用地批复（0=未批复 1=已批复 2=不需要）',
    construction_permit TINYINT DEFAULT 0 COMMENT '施工许可（0=未办理 1=已办理 2=不需要）',

    -- 项目负责人
    project_manager VARCHAR(100) COMMENT '项目负责人',
    manager_phone VARCHAR(50) COMMENT '负责人电话',
    manager_email VARCHAR(100) COMMENT '负责人邮箱',

    -- 营销价值
    marketing_priority TINYINT DEFAULT 3 COMMENT '营销优先级（1=高 2=中 3=低）',
    marketing_opportunity VARCHAR(1000) COMMENT '营销机会点（如贷款、结算、理财、投行等）',
    follow_up_plan VARCHAR(1000) COMMENT '跟进计划',

    -- 更新信息
    last_follow_up_date DATE COMMENT '最后跟进日期',
    next_follow_up_date DATE COMMENT '下次跟进日期',
    data_source VARCHAR(50) DEFAULT '客户经理录入' COMMENT '数据来源（客户经理录入、招标网、政府网站、企业公告等）',

    -- 备注
    remark VARCHAR(1000) COMMENT '备注',

    -- 多租户
    tenant_id BIGINT NOT NULL DEFAULT 1 COMMENT '租户ID',

    -- 审计字段
    creator VARCHAR(64) DEFAULT '' COMMENT '创建者',
    create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updater VARCHAR(64) DEFAULT '' COMMENT '更新者',
    update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    deleted BIT(1) NOT NULL DEFAULT b'0' COMMENT '是否删除（0=未删除 1=已删除）',

    -- 索引
    INDEX idx_customer_id (customer_id) COMMENT '客户ID索引',
    INDEX idx_project_type (project_type) COMMENT '项目类型索引',
    INDEX idx_project_status (project_status) COMMENT '项目状态索引',
    INDEX idx_financing_status (financing_status) COMMENT '融资状态索引',
    INDEX idx_marketing_priority (marketing_priority) COMMENT '营销优先级索引',
    INDEX idx_tenant_id (tenant_id) COMMENT '租户ID索引',
    INDEX idx_project_code (project_code) COMMENT '项目编号索引'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='对公客户项目信息表';

