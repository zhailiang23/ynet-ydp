<script lang="ts" setup>
import type { PayOrderApi } from '#/api/pay/order';

import { ref } from 'vue';

import { useVbenModal } from '@vben/common-ui';

import { getOrderDetail } from '#/api/pay/order';
import { useDescription } from '#/components/description';

import { useDetailSchema } from '../data';

const formData = ref<PayOrderApi.Order>();

const [Descriptions] = useDescription({
  componentProps: {
    border: true,
    column: 2,
    direction: 'horizontal',
    labelWidth: 140,
    title: '',
    extra: '',
  },
  schema: useDetailSchema(),
});

const [Modal, modalApi] = useVbenModal({
  async onOpenChange(isOpen: boolean) {
    if (!isOpen) {
      formData.value = undefined;
      return;
    }
    // 加载数据
    const data = modalApi.getData<PayOrderApi.Order>();
    if (!data || !data.id) {
      return;
    }
    modalApi.lock();
    try {
      formData.value = await getOrderDetail(data.id);
    } finally {
      modalApi.unlock();
    }
  },
});
</script>
<template>
  <Modal
    title="订单详情"
    class="w-1/2"
    :show-cancel-button="false"
    :show-confirm-button="false"
  >
    <Descriptions :data="formData" />
  </Modal>
</template>
